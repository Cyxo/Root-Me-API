def grey(msg):
    print(f'\033[90m[!] {msg}\033[00m')


def red(msg):
    print(f'\033[91m[!] {msg}\033[00m')


def green(msg):
    print(f'\033[92m[+] {msg}\033[00m')


def yellow(msg):
    print(f'\033[93m[+] {msg}\033[00m')


def blue(msg):
    print(f'\033[94m[*] {msg}\033[00m')


def purple(msg):
    print(f'\033[95m[*] {msg}\033[00m')


def cyan(msg):
    print(f'\033[96m[*] {msg}\033[00m')
